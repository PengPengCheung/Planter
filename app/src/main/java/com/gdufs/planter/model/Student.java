public class Student {

    private String mStuId;
    private String mStuName;
    private String mStuHeadUrl;

    public String getmStuId() {
        return mStuId;
    }

    public void setmStuId(String mStuId) {
        this.mStuId = mStuId;
    }

    public String getmStuName() {
        return mStuName;
    }

    public void setmStuName(String mStuName) {
        this.mStuName = mStuName;
    }

    public String getmStuHeadUrl() {
        return mStuHeadUrl;
    }

    public void setmStuHeadUrl(String mStuHeadUrl) {
        this.mStuHeadUrl = mStuHeadUrl;
    }
}
